SELECT A.CD_CLIENTE,A.NM_CLIENTE, MAX(TO_CHAR(A.DT_NEGOCIO, 'DD/MM/YYYY')),
B.CUSTODIA, TO_CHAR(C.CD_CPFCGC,'FM999999999999999'), C.CD_ASSESSOR, D.NM_ASSESSOR 
FROM DBMFINAN1 A, DBMFINAN33 B, TSCCLIBOL C, TSCASSES  D
WHERE A.IN_SITUAC = 'D' AND
A.VL_CORLIQ_TOT <> 0 AND
A.CD_CLIENTE = B.CD_CLIENTE AND
B.DATA_POSI = TRUNC(SYSDATE) AND
CUSTODIA <> 0 AND
A.CD_CLIENTE = C.CD_CLIENTE AND
c.tp_cliente in(01,02,03,18) AND
C.CD_ASSESSOR = D.CD_ASSESSOR 
GROUP BY A.CD_CLIENTE,A.NM_CLIENTE,B.CUSTODIA, C.CD_CPFCGC, C.CD_ASSESSOR, D.NM_ASSESSOR
/
