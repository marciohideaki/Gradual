SELECT A.CD_CLIENTE AS "CLIENTE",
    B.NOME_CLI AS "Nome", 
    B.COD_CART AS "Carteira",
    A.CUSTODIA AS "Custodia",
    B.COD_NEG AS "Papel",
    B.VAL_POSI AS "Valor Posicao",
    TO_CHAR(B.COD_CPF_CNPJ,'FM999999999999999') AS "CPF/CNPJ",
    B.COD_ASSE AS "Assessor",
    B.NOME_ASSE AS "Nome Assessor"
FROM DBMFINAN33 A, VCFPOSI_VALO B, TSCCLIBOL C
WHERE 
     A.CD_CLIENTE = B.COD_CLI     AND
     A.CD_CLIENTE = C.CD_CLIENTE AND
     C.TP_CLIENTE IN(01,02,03,18) AND
     A.DATA_POSI = TRUNC(SYSDATE)
ORDER by b.val_posi desc