select  distinct A.CD_CLIENTE, B.NM_CLIENTE,TO_CHAR(C.VALOR,'FM999999999999999999999D00'),
A.VL_DISPONIVEL, A.CUSTODIA,to_char(d.cd_cpfcgc,'fm999999999999999'), D.CD_ASSESSOR, E.NM_ASSESSOR
FROM DBMFINAN33 A, TSCCLIGER B, SFP04 C, TSCCLIBOL D, TSCASSES E
WHERE A.CD_CLIENTE = D.CD_CLIENTE AND
d.tp_cliente in(01,02,03,18) and
D.CD_ASSESSOR = E.CD_ASSESSOR AND
A.DATA_POSI = TRUNC(SYSDATE) AND
D.CD_CPFCGC= B.CD_CPFCGC AND
D.CD_CON_DEP = B.CD_CON_DEP AND
D.DT_NASC_FUND = D.DT_NASC_FUND AND
D.CD_CPFCGC = C.CD_CPFCGC
/
